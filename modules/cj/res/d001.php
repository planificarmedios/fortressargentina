        <strong> ARTÍCULO 1º.- PERSONAS EXPUESTAS POLITICAMENTE EXTRANJERAS. </strong></span></b><span
        style='font-size:6.0pt;line-height:107%'>
        Son consideradas Personas Expuestas Políticamente Extranjeras, los funcionarios públicos pertenecientes a países extranjeros, que se 
        desempeñen o se hayan desempeñado, en alguno de los cargos que se detallan a continuación:<br>
        a.	Jefe de Estado, jefe de Gobierno, Gobernador, Intendente, Ministro, Secretario, Subsecretario de Estado u otro cargo gubernamental equivalente.
        b.	Miembro del Parlamento, Poder Legislativo, o de otro órgano de naturaleza equivalente.<br>
        c.	Juez, Magistrado de Tribunales Superiores u otra alta instancia judicial, o administrativa, en el ámbito del Poder Judicial.<br>
        d.	Embajador o cónsul, de un país u organismo internacional.<br>
        e.	Autoridad, apoderado, integrantes del órgano de administración o control y miembros relevantes de partidos políticos extranjeros.<br>
        f.	Oficial de alto rango de las fuerzas armadas (a partir de coronel o grado equivalente en la fuerza y/o país de que se trate) o de las fuerzas de seguridad pública (a partir de comisario o rango equivalente según la fuerza y/o país de que se trate). <br>
        g.	Miembro de los órganos de dirección y control de empresas de propiedad estatal.<br>
        h.	Miembro de los órganos de dirección o control de empresas de propiedad privada o mixta; cuando el Estado posea una participación igual o superior al VEINTE POR CIENTO (20%) del capital o del derecho a voto, o ejerza de forma directa o indirecta el control de la compañía.<br>
        i.	Director, gobernador, consejero, síndico o autoridad equivalente de bancos centrales y otros organismos de regulación y/o supervisión.<br>
        j.	Director, subdirector; miembro de la junta, directorio, alta gerencia, o cargos equivalentes, apoderados, representantes legales o autorizados, de una organización internacional, 
            con facultades de decisión, administración o disposición. <br><br>

        <strong> ARTÍCULO 2º.- PERSONAS EXPUESTAS POLITICAMENTE NACIONALES.</strong><br>
        Son consideradas Personas Expuestas Políticamente Nacionales, los funcionarios públicos del país que se desempeñen o se hayan desempeñado en alguno de los siguientes cargos: <br>
        a.	Presidente o Vicepresidente de la Nación.<br>
        b.	Senador o Diputado de la Nación.<br>
        c.	Magistrado del Poder Judicial de la Nación.<br>
        d.	Magistrado del Ministerio Público de la Nación.<br>
        e.	Defensor del Pueblo de la Nación o Defensor del Pueblo Adjunto.<br>
        f.	Jefe de Gabinete de Ministros, Ministro, Secretario o Subsecretario del Poder Ejecutivo Nacional.<br>
        g.	Interventor federal, o colaboradores del interventor federal con categoría no inferior a Director o su equivalente.<br>
        h.	Síndico General de la Nación o Síndico General Adjunto de la Sindicatura General de la Nación; Presidente o Auditor General de la Auditoría General de la Nación; autoridad superior de un ente regulador o de los demás órganos que integran los sistemas de control del sector público nacional; miembros de organismos jurisdiccionales administrativos, o personal de dicho organismo, con categoría no inferior a la de director o su equivalente.<br>
        i.	Miembro del Consejo de la Magistratura de la Nación o del Jurado de Enjuiciamiento.<br>
        j.	Embajador o Cónsul.<br>
        k.	Personal de las Fuerzas Armadas, de la Policía Federal Argentina, de Gendarmería Nacional, de la Prefectura Naval Argentina, del Servicio Penitenciario Federal o de la Policía de Seguridad Aeroportuaria con jerarquía no menor de coronel o grado equivalente según la fuerza.
        l.	Rector, Decano o Secretario de las Universidades Nacionales.
        m.	Funcionario o empleado con categoría o función no inferior a la de Director General o Nacional, de la Administración Pública Nacional, centralizada o descentralizada, de entidades autárquicas, bancos y entidades financieras del sistema oficial, de las obras sociales administradas por el Estado, de empresas del Estado, las sociedades del Estado y el personal con similar categoría o función, designado a propuesta del Estado en sociedades de economía mixta, sociedades anónimas con participación estatal o en otros entes del sector público. <br>
        n.	Funcionario o empleado público nacional encargado de otorgar habilitaciones administrativas, permisos o concesiones, para el ejercicio de cualquier actividad; como así también todo funcionario o empleado público encargado de controlar el funcionamiento de dichas actividades o de ejercer cualquier otro control en virtud de un poder de policía. <br>
        o.	Funcionario público de algún organismo de control de servicios públicos, con categoría no inferior a la de Director General o Nacional. <br>
        p.	Personal del Poder Legislativo de la Nación, con categoría no inferior a la de Director. <br>
        q.	Personal del Poder Judicial de la Nación o del Ministerio Público de la Nación, con categoría no inferior a Secretario. <br>
        r.	Funcionario o empleado público que integre comisiones de adjudicación de licitaciones, de compra o de recepción de bienes, o participe en la toma de decisiones de licitaciones o compras. <br>
        s.	Funcionario público responsable de administrar un patrimonio público o privado, o controlar o fiscalizar los ingresos públicos cualquiera fuera su naturaleza. <br>
        t.	Director o Administrador de alguna entidad sometida al control externo del Honorable Congreso de la Nación, 
        de conformidad con lo dispuesto en el artículo 120 de la Ley Nº 24.156. <br><br>
        
        <strong> ARTÍCULO 3º.- PERSONAS EXPUESTAS POLITICAMENTE PROVINCIALES, MUNICIPALES Y DE LA CIUDAD AUTONOMA DE BUENOS AIRES.</strong> <br>
        Son consideradas Personas Expuestas Políticamente, los funcionarios públicos que se desempeñen o se hayan desempeñado en alguno de los siguientes cargos, a nivel Provincial, Municipal o de la Ciudad Autónoma de Buenos Aires: <br>
        a.	Gobernador o Vicegobernador, Intendente o Vice-intendente, Jefe de Gobierno o Vicejefe de Gobierno. <br>
        b.	Ministro de Gobierno, Secretario, Subsecretario, Ministro de los Tribunales Superiores de Justicia de las provincias o de la Ciudad Autónoma de Buenos Aires. <br>
        c.	Juez o Secretario de los Poderes Judiciales Provinciales o de la Ciudad Autónoma de Buenos Aires. <br>
        d.	Magistrado perteneciente al Ministerio Público, o su equivalente, en las provincias o en la Ciudad Autónoma de Buenos Aires. <br>
        e.	Miembro del Consejo de la Magistratura o del Jurado de Enjuiciamiento, o su equivalente, de las Provincias o de la Ciudad Autónoma de Buenos Aires. <br>
        f.	Defensor del Pueblo o Defensor del Pueblo Adjunto, en las Provincias o en la Ciudad Autónoma de Buenos Aires. <br>
        g.	Jefe de Gabinete de Ministros, Ministro, Secretario o Subsecretario del Poder Ejecutivo de las Provincias o de la Ciudad Autónoma de Buenos Aires. <br>
        h.	Legislador provincial, municipal o de la Ciudad Autónoma de Buenos Aires. <br>
        i.	Máxima autoridad de los organismos de control o de los entes autárquicos provinciales, municipales o de la Ciudad Autónoma de Buenos Aires. <br>
        j.	Máxima autoridad de las sociedades de propiedad de los estados provinciales, municipales o de la Ciudad Autónoma de Buenos Aires. <br>
        k.	Rector, Decano o Secretario de universidades provinciales. <br>
        l.	Funcionario o empleado público encargado de otorgar habilitaciones administrativas, permisos o concesiones, para el ejercicio de cualquier actividad; como así también   todo funcionario o empleado público encargado de controlar el funcionamiento de dichas actividades o de ejercer cualquier otro control en virtud de un poder de policía. <br>
        m.	Funcionario de organismos de control de los servicios públicos provinciales o de la Ciudad de Buenos Aires, con categoría no inferior a la de Director General o Provincial. <br>
        n.	Funcionario o empleado público que integre comisiones de adjudicación de licitaciones, de compra o de recepción de bienes, o participe en la toma de decisiones de licitaciones o compras. <br>
        o.	Funcionario público que tenga por función administrar un patrimonio público o privado, o controlar o fiscalizar los ingresos públicos cualquiera fuera su naturaleza. <br><br>
        
        <strong> ARTÍCULO 4º.- OTRAS PERSONAS EXPUESTAS POLITICAMENTE.</strong> <br>
        Sin perjuicio de lo expuesto en los artículos precedentes, son consideradas Personas Expuestas Políticamente aquellas personas que se desempeñen o se hayan desempeñado en alguno de los siguientes cargos: <br>
        a.	Autoridad, apoderado, candidato o miembro relevante de partidos políticos o alianzas electorales, ya sea a nivel nacional o distrital, de conformidad con lo establecido en las Leyes N° 23.298 y N° 26.215. <br>
        b.	Autoridad de los órganos de dirección y administración de organizaciones sindicales y empresariales (cámaras, asociaciones y otras formas de agrupación corporativa). <br>
        Con respecto a las organizaciones sindicales, el alcance comprende a las personas humanas con capacidad de decisión, administración, control o disposición del patrimonio de la organización sindical. <br>
        Con respecto a las organizaciones empresariales, el alcance comprende a las personas humanas de las mencionadas organizaciones que, en función de su cargo: <br>
        1.	tengan capacidad de decisión, administración, control o disposición sobre fondos provenientes del sector público nacional, provincial, municipal o de la Ciudad Autónoma de Buenos Aires, o  2.	realicen actividades con fines de lucro, para la organización o sus representados, que involucren la gestión, intermediación o contratación habitual con el Estado nacional, provincial, municipal o de la Ciudad Autónoma de Buenos Aires.
        (Inciso b) sustituido por art. 1° de la Resolución N° 15/2019 de la Unidad de Información Financiera B.O. 25/02/2019)
        c.	Autoridad, representante legal o integrante de la Comisión Directiva de las obras sociales contempladas en la Ley Nº 23.660. El alcance comprende a las personas humanas de las mencionadas organizaciones con capacidad de decisión, administración, control o disposición del patrimonio de las obras sociales.
        d.	Las personas humanas con capacidad de decisión, administración, control o disposición del patrimonio de personas jurídicas privadas en los términos del 148 del Código Civil y Comercial de la Nación, que reciban fondos públicos destinados a terceros y cuenten con poder de control y disposición respecto del destino de dichos fondos.
        (Inciso d) sustituido por art. 2° de la Resolución N° 15/2019 de la Unidad de Información Financiera B.O. 25/02/2019)<br><br>
        
        <strong> ARTÍCULO 5º.- PERSONAS EXPUESTAS POLITICAMENTE POR CERCANÍA O AFINIDAD.</strong> <br>
        Son consideradas Personas Expuestas Políticamente por cercanía o afinidad, todos aquellos sujetos que posean vínculos personales o jurídicos con quienes cumplan, o hayan cumplido, las funciones establecidas en los artículos 1° a 4° de la presente.
        A los fines indicados se consideran los siguientes vínculos:
        a.	Cónyuge o conviviente reconocido legalmente.
        b.	Familiares en línea ascendente, descendente, y colateral hasta el segundo grado de consanguinidad o afinidad.
        (Inciso b) sustituido por art. 3° de la Resolución N° 15/2019 de la Unidad de Información Financiera B.O. 25/02/2019)
        c.	Personas allegadas o cercanas: debe entenderse como tales a aquellas personas públicas y comúnmente conocidas por su íntima asociación a la persona definida como Persona Expuesta Políticamente.
        d.	Personas con las cuales se hayan establecido relaciones jurídicas de negocios del tipo asociativa, aún de carácter informal, cualquiera fuese su naturaleza.
        e.	Toda otra relación o vínculo que por sus características y en función de un análisis basado en riesgo, a criterio del sujeto obligado, pueda resultar relevante.

                
          
